use std::error::Error;
use std::time::Duration;

/// Combined test: send the PROVEN-working 0x5d static-color sequence
/// (interface 0, the exact bytes captured live when `asusctl aura effect
/// static -c 0000ff` visibly turned the keyboard blue -- see HANDOFF.md
/// "Linux session 2"), then IMMEDIATELY send a 0x04 zone-colour packet on
/// interface 1 -- on the theory that a real, successful 0x5d negotiation
/// might leave the controller in a receptive state for 0x04, unlike every
/// prior 0x04 test which was always sent cold/standalone.
///
/// Note: the packet lengths/content here are NOT a retry of the padding
/// idea already ruled out (see HANDOFF.md) -- 0x5d is sent at its proven
/// 64-byte length, 0x04 at its proven 51-byte length. Only the sequencing
/// (0x5d immediately before 0x04) is new.
fn main() -> Result<(), Box<dyn Error>> {
    let devices = rusb::devices()?;
    let mut target = None;
    for device in devices.iter() {
        let desc = device.device_descriptor()?;
        if desc.vendor_id() == 0x0B05 && desc.product_id() == 0x19B6 {
            target = Some(device);
            break;
        }
    }
    let device = target.ok_or("device 0B05:19B6 not found")?;
    let handle = device.open()?;

    // --- Step 1: interface 0, the proven-working 0x5d sequence ---
    let iface0: u8 = 0;
    let had_driver0 = handle.kernel_driver_active(iface0).unwrap_or(false);
    if had_driver0 {
        println!("Detaching kernel driver from interface 0...");
        handle.detach_kernel_driver(iface0)?;
    }
    handle.claim_interface(iface0)?;

    // Exact captured bytes: b3 (set effect, Static mode, blue), b5, b4.
    #[rustfmt::skip]
    let b3: [u8; 64] = [
        0x5d, 0xb3, 0x00, 0x00, 0x00, 0x00, 0xff, 0xeb, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    ];
    #[rustfmt::skip]
    let b5: [u8; 64] = [
        0x5d, 0xb5, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    ];
    #[rustfmt::skip]
    let b4: [u8; 64] = [
        0x5d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
    ];

    for (label, pkt) in [("b3 (effect)", &b3), ("b5 (set)", &b5), ("b4 (apply)", &b4)] {
        let r = handle.write_control(0x21, 0x09, 0x025d, iface0 as u16, pkt, Duration::from_secs(2));
        println!("0x5d {label}: {r:?}");
    }

    let _ = handle.release_interface(iface0);
    if had_driver0 {
        let _ = handle.attach_kernel_driver(iface0);
    }

    println!("0x5d sequence complete -- keyboard should be blue now. Immediately sending 0x04...");

    // --- Step 2: interface 1, the proven-format 0x04 zone packet ---
    let iface1: u8 = 1;
    let had_driver1 = handle.kernel_driver_active(iface1).unwrap_or(false);
    if had_driver1 {
        handle.detach_kernel_driver(iface1)?;
    }
    handle.claim_interface(iface1)?;

    #[rustfmt::skip]
    let zone_packet: [u8; 51] = [
        0x04, 0x01, 0x01, 0x06, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0xff, 0x00, 0x00, 0xff, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    ];
    let r = handle.write_control(0x21, 0x09, 0x0304, iface1 as u16, &zone_packet, Duration::from_secs(2));
    println!("0x04 zone 0x06 (back-left corner) red: {r:?}");

    let _ = handle.release_interface(iface1);
    if had_driver1 {
        let _ = handle.attach_kernel_driver(iface1);
    }

    println!("Done. Keyboard should be blue (from 0x5d). Check the back-left corner for red (from 0x04).");
    Ok(())
}
